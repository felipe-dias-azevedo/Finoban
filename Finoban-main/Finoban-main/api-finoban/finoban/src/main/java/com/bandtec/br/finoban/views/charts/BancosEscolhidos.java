package com.bandtec.br.finoban.views.charts;

public class BancosEscolhidos {

    private Integer cifra;
    private Integer s16;
    private Integer presil;

    public BancosEscolhidos() {
        this.cifra = 0;
        this.s16 = 0;
        this.presil = 0;
    }

    public Integer getCifra() {
        return cifra;
    }

    public void setCifra(Integer cifra) {
        this.cifra = cifra;
    }

    public Integer getS16() {
        return s16;
    }

    public void setS16(Integer s16) {
        this.s16 = s16;
    }

    public Integer getPresil() {
        return presil;
    }

    public void setPresil(Integer presil) {
        this.presil = presil;
    }
}
