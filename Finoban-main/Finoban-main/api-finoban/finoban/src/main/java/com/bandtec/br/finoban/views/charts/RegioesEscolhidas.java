package com.bandtec.br.finoban.views.charts;

public class RegioesEscolhidas {

    private Integer centro;
    private Integer consolacao;
    private Integer brooklin;
    private Integer mooca;
    private Integer santoAmaro;
    private Integer interlagos;

    public RegioesEscolhidas() {
        this.centro = 0;
        this.consolacao = 0;
        this.brooklin = 0;
        this.mooca = 0;
        this.santoAmaro = 0;
        this.interlagos = 0;
    }

    public Integer getCentro() {
        return centro;
    }

    public void setCentro(Integer centro) {
        this.centro = centro;
    }

    public Integer getConsolacao() {
        return consolacao;
    }

    public void setConsolacao(Integer consolacao) {
        this.consolacao = consolacao;
    }

    public Integer getBrooklin() {
        return brooklin;
    }

    public void setBrooklin(Integer brooklin) {
        this.brooklin = brooklin;
    }

    public Integer getMooca() {
        return mooca;
    }

    public void setMooca(Integer mooca) {
        this.mooca = mooca;
    }

    public Integer getSantoAmaro() {
        return santoAmaro;
    }

    public void setSantoAmaro(Integer santoAmaro) {
        this.santoAmaro = santoAmaro;
    }

    public Integer getInterlagos() {
        return interlagos;
    }

    public void setInterlagos(Integer interlagos) {
        this.interlagos = interlagos;
    }
}
